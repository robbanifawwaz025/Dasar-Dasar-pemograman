def celsius_ke_fahrenheit(celsius):
  """Mengkonversi suhu dari Celsius ke Fahrenheit."""
  fahrenheit = (celsius * 9/5) + 32
  return fahrenheit

print(f"celsius_ke_fahrenheit(0): {celsius_ke_fahrenheit(0)}")
print(f"celsius_ke_fahrenheit(100): {celsius_ke_fahrenheit(100)}")
print(f"celsius_ke_fahrenheit(32): {celsius_ke_fahrenheit(32)}")

    
